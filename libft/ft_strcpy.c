#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	i;

	if (dst != NULL && src != NULL)
	{
		i = 0;
		while (src[i] != '\0')
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
		return (dst);
	}
	return (NULL);
}
