#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*s_zero;

	if (s != NULL)
	{
		i = 0;
		s_zero = s;
		if (n > 0)
		{
			while (i < n)
			{
				s_zero[i] = 0;
				i++;
			}
		}
	}
}
